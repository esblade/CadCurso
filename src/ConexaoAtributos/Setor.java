package ConexaoAtributos;

public class Setor {
	private int cod_setor;
	private String dsc_setor;
	private String nom_setor;
	
	public int getCod_setor() {
		return cod_setor;
	}
	public void setCod_setor(int cod_setor) {
		this.cod_setor = cod_setor;
	}
	public String getDsc_setor() {
		return dsc_setor;
	}
	public void setDsc_setor(String dsc_setor) {
		this.dsc_setor = dsc_setor;
	}
	public String getNom_setor() {
		return nom_setor;
	}
	public void setNom_setor(String nom_setor) {
		this.nom_setor = nom_setor;
	}
}